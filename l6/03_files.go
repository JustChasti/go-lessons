package main

import (
	"fmt"
	"os"
)

// Урок 6.3 — ДЕМО: файлы и режимы доступа.
// Теория — в README.md.
//
// В уроке 4 мы уже использовали os.ReadFile/os.WriteFile для JSON.
// Здесь — короткое напоминание плюс главное, что не разбирали:
// права доступа (perm) и флаги открытия (O_APPEND, O_CREATE и т.п.).

func filesDemo() {
	const path = "demo.txt"

	// 1. WriteFile — самый простой способ записать файл целиком.
	// Третий аргумент — права доступа в восьмеричной записи.
	//   0644 = -rw-r--r--  владелец читает+пишет, остальные только читают
	//   0600 = -rw-------  только владелец, никто другой не прочитает
	//   0666 = -rw-rw-rw-  все читают и пишут (так почти не делают)
	// На Windows права работают ограниченно, но указывать их всё равно нужно.
	if err := os.WriteFile(path, []byte("первая строка\n"), 0644); err != nil {
		fmt.Println("ошибка записи:", err)
		return
	}

	// 2. ReadFile — читаем содержимое целиком в []byte.
	// Подходит для конфигов и небольших дампов. Большие файлы лучше стримить
	// через os.Open + bufio.Scanner — но это тема отдельная.
	data, err := os.ReadFile(path)
	if err != nil {
		fmt.Println("ошибка чтения:", err)
		return
	}
	fmt.Println("прочитано:")
	fmt.Print(string(data))

	// 3. OpenFile — низкоуровневый доступ, когда хочется не «всё или ничего»,
	// а конкретный режим. Флаги комбинируются через побитовое ИЛИ (`|`).
	//   os.O_RDONLY — только чтение
	//   os.O_WRONLY — только запись
	//   os.O_RDWR   — чтение и запись
	//   os.O_APPEND — дописывать в конец, не перезаписывать
	//   os.O_CREATE — создать, если файла нет
	//   os.O_TRUNC  — обрезать до 0 при открытии (стереть всё, что было)
	//   os.O_EXCL   — вместе с O_CREATE: ошибка, если файл уже существует
	//
	// Здесь — открываем существующий файл и дописываем строку в конец.
	f, err := os.OpenFile(path, os.O_APPEND|os.O_WRONLY, 0644)
	if err != nil {
		fmt.Println("ошибка открытия:", err)
		return
	}
	// Файл — это ресурс ОС (файловый дескриптор). Закрывать обязательно,
	// иначе при долгой работе дескрипторы кончатся.
	defer f.Close()

	if _, err := f.WriteString("вторая строка (дописали)\n"); err != nil {
		fmt.Println("ошибка дозаписи:", err)
		return
	}

	// Перечитываем файл, чтобы увидеть результат.
	data, _ = os.ReadFile(path)
	fmt.Println("\nпосле дозаписи:")
	fmt.Print(string(data))

	// 4. Удаление — чтобы не оставлять мусор после демо.
	if err := os.Remove(path); err != nil {
		fmt.Println("ошибка удаления:", err)
	}
}
